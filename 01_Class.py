""" Class is called as the blueprint for the object. If you want to create a class then 
you need to use class keyword then write the name of the class and finally give a colon(:) """

''' Method-I '''
class SSC():
    a=100
    b=25
    c=2000
print(SSC().a)
print(SSC().b)
print(SSC().c)

''' Method-II '''
class shop():
    dall = 25
    myda = 15
    coriander = 13
    oil = 154
    coconut = 25
s = shop()
